/// Defines shared animation and transition durations for the design system.
///
/// These values keep motion behavior consistent across the app and make it
/// easier to adjust transition timing from one place.
class AppDurations {
  AppDurations._();

  static const Duration short = Duration(milliseconds: 150);
  static const Duration medium = Duration(milliseconds: 250);
  static const Duration long = Duration(milliseconds: 400);
}
